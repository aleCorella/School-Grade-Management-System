    private void BtnIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresoActionPerformed
       JOptionPane.showMessageDialog(null, "Cargando...");
        
        String nombreIngre = nameIngre.getText();
        String apellidoIngre = apellidoIngre1.getText();
        int notaIngre = Integer.parseInt(numIngre.getText());
        String fullName= nombreIngre+ " " +apellidoIngre;
        MetosAlumnos.ingresarDatos(fullName, notaIngre);
        MetosAlumnos.mostrarLista();
        
        // Validar los datos de entrada
        if (nombreIngre.isEmpty() ||apellidoIngre.isEmpty() || notaIngre < 0 || notaIngre > 100) {
        JOptionPane.showMessageDialog(null, "Error: los datos ingresados no son v√°lidos");
        return;
        }   
    
        // Insertar los datos en la pila
        
        for (int i = 0; i < nombreIngre.length(); i++) {
        int n = randi.nextInt(999) + 1;
        pila.insertar(notaIngre, n, fullName);
            
        }
        
        // Limpiar los campos de entrada
        nameIngre.setText("");
        apellidoIngre1.setText("");
        numIngre.setText("");
    
//        // Mostrar la pila en la consola
        
    
    }//GEN-LAST:event_BtnIngresoActionPerformed

