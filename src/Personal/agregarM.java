    public void keyTyped(KeyEvent e) {
        char c = e.getKeyChar();
        if (!Character.isLetter(c)) {
            e.consume(); // Deshabilitar evento keyTyped
        }

    }

    public void keyTypedNums(KeyEvent e) {
        char c = e.getKeyChar();
        if (!Character.isDigit(c)) {
            e.consume(); // Deshabilitar evento keyTyped
        }
    }

    public void HabilitarBoton() {

        if (!nameIngre.getText().isEmpty() && !apellidoIngre1.getText().isEmpty() && !numIngre.getText().isEmpty()) {
            BtnIngreso.setEnabled(true);
        } else {
            BtnIngreso.setEnabled(false);
        }
    }
